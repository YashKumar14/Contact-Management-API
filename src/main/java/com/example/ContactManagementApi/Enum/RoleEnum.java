package com.example.ContactManagementApi.Enum;

/**
 * Enum representing the various roles that can be assigned to users in the
 * application. This enum is used to manage user permissions and access control
 * throughout the application.
 */
public enum RoleEnum {

	/**
	 * Represents a standard user role with basic permissions. Users with this role
	 * can access general application features.
	 */
	ROLE_USER,

	/**
	 * Represents an administrator role with elevated permissions. Admin users have
	 * access to administrative functions and settings within the application.
	 */
	ROLE_ADMIN

}
